import java.util.HashSet;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode(int x) { val = x; }
 * }
 */
public class TreeNode {
    int      val;
    TreeNode left;
    TreeNode right;

    TreeNode(int x) {
        val = x;
    }
}

class Solution {

    public boolean isUnivalTree(TreeNode root) {

        Set<Integer> res = new HashSet<>();
        if (root == null)
            return false;
        dfs(root, res);
        return res.size() == 1;
    }

    void dfs(TreeNode root, Set<Integer> res) {
        if (root == null)
            return;

        res.add(root.val);
        dfs(root.left, res);
        dfs(root.right, res);

    }

}
